int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned char	r1;
	unsigned char	r2;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n - 1 && s1[i] != '\0' && s1[i] == s2[i])
		i++;
	r1 = s1[i];
	r2 = s2[i];
	return ((unsigned char)r1 - (unsigned char)r2);
}
